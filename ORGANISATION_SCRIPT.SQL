CREATE TABLE ORGANISATION
(
  OG_NO         NUMBER(30),
  OG_ID         VARCHAR2(50) CONSTRAINT OG_N01 NOT NULL,
  OG_NAME       VARCHAR2(100) CONSTRAINT OG_N02 NOT NULL,
  ADDRESS       VARCHAR2(4000),
  OG_TEL        VARCHAR2(100),
  OG_MOB        VARCHAR2(100),
  OG_EMAIL      VARCHAR2(100),
  OG_WEBSITE    VARCHAR2(100),
  PARENT_OG_NO  NUMBER(30),
  ACTIVE_FLAG   NUMBER(1)                  DEFAULT 0 CONSTRAINT OG_N04 NOT NULL,
  LAUNCHED_ON   DATE,
  EXPIRED_ON         DATE,
  LCN_EXP_MSG_DAY    NUMBER,
  LCN_KEY            VARCHAR2(100),
  LCN_VALUE          VARCHAR2(100),
  OG_ALIAS           VARCHAR2(30),
  OG_SLOGAN          VARCHAR2(100),
  ALT_ADDRESS        VARCHAR2(4000),
  ADD_UNION          NUMBER(30),
  ADD_THANA          NUMBER(30),
  ADD_DISTRICT       NUMBER(30),
  ADD_DIVISION       NUMBER(30),
  ADD_PROVINCE       NUMBER(30),
  ADD_COUNTRY        NUMBER(30),
  TIN_NO             VARCHAR2(100),
  VAT_REG_NO         VARCHAR2(100),
  LICENSE_NO         VARCHAR2(100),
  SS_CREATOR         NUMBER(30),
  SS_CREATED_ON      DATE,
  SS_MODIFIER        NUMBER(30),
  SS_MODIFIED_ON     DATE,
  SS_OG_NO      NUMBER(30)
)

CREATE UNIQUE INDEX OG_PK ON OG
(OG_NO);

CREATE UNIQUE INDEX OG_U01 ON OG
(OG_ID);

ALTER TABLE OG ADD (
  CONSTRAINT OG_PK
  PRIMARY KEY
  (OG_NO)
  USING INDEX OG_PK
  ENABLE VALIDATE,
  CONSTRAINT OG_U01
  UNIQUE (OG_ID)
  USING INDEX OG_U01
  ENABLE VALIDATE);
